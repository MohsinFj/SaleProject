    private void txtDollarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDollarKeyReleased
        if(!txtDollar.getText().equals(""))
        {
            if(!Character.isDigit(txtDollar.getText().charAt(txtDollar.getText().length()-1)))
            {
                txtDollar.setText(""+txtDollar.getText().substring(0, txtDollar.getText().length()- 1));
                return;
            }
            if(!Character.isDigit(txtDollar.getText().charAt(0)))
            {
                txtDollar.setText(""+txtDollar.getText().substring(1));
            }
        }
    }//GEN-LAST:event_txtDollarKeyReleased


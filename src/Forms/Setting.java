    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedIndex = cmbGrop.getSelectedIndex();
        if(selectedIndex == -1)
        {
            JOptionPane.showMessageDialog(null, "الرجاء اختيار العنصر المراد حذفه");
        }
        else
        {
            Object name = cmbGrop.getSelectedItem();
            int dialogResult = JOptionPane.showConfirmDialog(null, "هن انت متأكد من حذف هذا العنصر", "انتبه", JOptionPane.YES_NO_OPTION);
            if(dialogResult == 0)
            {
                String sql = "DELETE FROM grop WHERE Name ='" + name + "'";
                String result = g.query(sql);

                JOptionPane.showMessageDialog(null, "تم الحذف");

                cmbGrop.removeItemAt(selectedIndex);
                cmbGrop.setSelectedIndex(-1);
            }
            else
            {
               cmbGrop.setSelectedIndex(-1);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

